firstInput = int(input("Put the first numbers: "))
secondInput = int(input("Put the second numbers: "))

operationResult = firstInput + secondInput
print(f"The result from {firstInput} + {secondInput} = {operationResult}")

operationResult = firstInput - secondInput
print(f"The result from {firstInput} - {secondInput} = {operationResult}")

operationResult = firstInput * secondInput
print(f"The result from {firstInput} * {secondInput} = {operationResult}")

operationResult = firstInput / secondInput
print(f"The result from {firstInput} / {secondInput} = {operationResult}")

operationResult = firstInput % secondInput
print(f"The result from {firstInput} % {secondInput} = {operationResult}")

operationResult = firstInput ** secondInput
